#include <stdio.h>

int main(int argc, char **argv)
{
    // v1 v3 is a random value    
    int v1, v2 = 9, v3;

    // Specified as an integer variable, the initial value is 3
    int a = 3;

    // Specified as a floating point variable, the initial value is 3.56
    float f = 3.56f;

    // Specified as a character variable, the initial value is 'a'
    char c = 'a';

    printf("a = %d\n", a);
    printf("f = %f\n", f);
    printf("c = %c\n", c);

    printf("=============================================\n");

    printf("v1 = %d v2 = %d v3 = %d\n", v1, v2, v3);




    return 0;
}
